			<h1>Community Outreach</h1>
			<h2>Food Closet and Clothes Closet</h2>
			<p>The Food Closet is a community outreach program which provides food to those in need within the Forestville community.  The Food Closet is open the second and fourth Monday of each month at 1pm.  Clients need only to register once for the year. The Clothes Closet opens at noon on the fourth Monday of each month and provides gently used clothing to our Food Closet clients.  Donations of new or lightly used clothing are always welcome. </p>
			<h2>English as a Second Language Classes</h2>
			<p/>Forestville United Methodist Church offers beginning and intermediate English as a Second Language classes for those in the Forestville community.  Classes are held from noon to 1pm on the days of the Food Closet, the second and fourth Monday of each month. Child care is available. </p>
			<h2>Grace Café</h2>
			<p>Grace Café is an after school program for Middle School students held on Tuesdays from 2-4pm while school is in session. It offers students the opportunity for bible study, homework support, computer access, creative arts and fellowship.  </p>
			<h2>Forestville UMC is home to the following groups:</h2>
