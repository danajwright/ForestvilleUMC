			<h1>Church History</h1>
			<p>We trace our United Methodist spiritual heritage to the 18th century evangelical leader John Wesley who saw our work as Christians in these simple and plain terms:  Do all the good you can, in all the places you can, to all the people you can. Methodists gathered in Sonoma County in 1850 and resolved to begin reaching out to families in various Russian River communities. The Forestville congregation had its roots in an early Green Valley camp meeting.</p>
			<br>
			<p>On April 4, 1884 a schoolhouse property was purchased by the Trustees of the Forestville Church.  The first Forestville Methodist Church building was a converted store, which stood on the west side of the present facility.  Our church building on Covey Road was dedicated on August 24, 1924. </p>
			<br>
			<p>Today we serve Forestville families in many ways by providing a Food and Clothing Closet; teaching English as a second language and by offering spiritual guidance through worship, Bible Study, music ministry and hands-on service projects such as Habitat for Humanity.  We consider it part of our ministry to offer hospitality to community groups who contribute to the well-being of our community.</p>
