			<h1>Ministries</h1>
			<h2>United Methodist Women (UMW)</h2>
			<p>The United Methodist Women is a community of women whose purpose is to know God, and to experience freedom as whole persons through Jesus Christ; to develop a creative, supportive fellowship, and to expand concepts of mission through participation in the global ministries of the Church.  The UMW is open to all women who wish to participate. Meetings are held the first Monday of every month at 12:15 in Weibe Hall. </p>
			<h2>Kallah</h2>
			<p>The women of Kallah are an extension of the UMW.  They work to support local ministries, causes and assist financially with church projects.  Kallah’s main fund raiser is the annual Rummage Sale held each March.  Kallah meets the 4th Monday of every month at 7pm in the Fireside Room. </p>
			<h2>Stephen Ministry</h2>
			<p>Stephen Ministry is a confidential one on one care giving ministry. Each Stephen Minister is trained to help those in need by listening, exploring feelings, praying and sharing Christ’s love.  They help those experiencing loss, in crisis, hospitalized and grieving by planting a seed of hope.</p>
			<h2>United Methodist Men (UMM)</h2>
			<p>The mission of the United Methodist Men is to support spiritual growth among men, helping them to mature as disciples as they encourage spiritual formation in others. The UMW is involved in Scouting, Habitat for Humanity and supports community programs benefitting those in need.  </p>
			<h2>Earthkeepers</h2>
			<p>The Earthkeepers of the Forestville United Methodist believe we need to treat creation with reverence, care, and humility because we are utterly dependent upon it. They encourage everyone to act responsibly in all they do during their time on earth, to ensure future generations will be able to continue to enjoy the Earth and its wonders. Earthkeepers  raise awareness to issues connected with taking good care of the Earth. They celebrate God's power and His amazing handiwork. The Earthkeepers understand that “The earth is the Lord's” and that "God saw all that He had made and it was very good."
			<h2>Fellowship</h2>
			<p>The Fellowship Ministry at FUMC sponsors a variety of events to bring together people from the church and the local community in fellowship activities. Events have included the annual Variety Show, Tea and Classics, Madrigal Dinner, Bocce Ball, Game Night, Black & White Ball, Mystery Theater, Sock Hop, Heritage and Travel Nights with meals and more.  There are also special Sundays - Hat, T-Shirt, Hawaiian Shirt, Apple Pie, Taco Soup - to build camaraderie in Christian spirit with others.  </p>
